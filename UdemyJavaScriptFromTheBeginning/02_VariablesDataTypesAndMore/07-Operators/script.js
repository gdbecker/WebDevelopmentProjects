// 1. Arithmetic Operators
let x;
x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
console.log(x);

// Concatenation
let y;
y = "Hello" + " " + "World";
console.log(y);

// Exponent
let u = 2 ** 3;
console.log(u);

// Increment
let i = 9;
i++;
console.log(i);

// Decrement
let r = 8;
r--;
console.log(r);

// 2. Assignment Operators
let q = 80;
q += 9;
q -= 7;
q *= 8;
q %= 5;
console.log(q);

// 3. Comparison Operators
// == tests for the value
let s = 2 == "2";
console.log(s);

// === tests for value and type
let j = 2 === "2";
console.log(j);

let f = 2 != 9;
console.log(f);

let h = 2 !== 2;
console.log(h);

let n = 9 > 8;
console.log(n);

n = 7 < 90;
console.log(n);
