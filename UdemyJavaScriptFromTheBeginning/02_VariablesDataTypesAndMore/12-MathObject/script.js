// Math is a built-in JavaScript object

// View methods available
console.log(Math);

let x;

// Various Math object methods
x = Math.sqrt(9);

x = Math.abs(-5);

x = Math.round(4.6);
x = Math.round(4.23);

x = Math.ceil(5.2);
x = Math.floor(7.9);

x = Math.pow(2, 3);

x = Math.min(4, 5, 6);
x = Math.max(4, 5, 6);

x = Math.random(); // random decimal between 0 and 1
x = Math.random() * 10; // between 0-9
x = Math.floor(Math.random() * 10 + 1) // between 1-10

console.log(x);